using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.IO;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CopyFile
{
    class FileSharp
    {
        public static bool connectState(string path)
        {
            return connectState(path, "", "");
        }

        public static bool connectState(string path, string userName, string passWord)
        {
            bool Flag = false;
            Process proc = new Process();
            try
            {
                proc.StartInfo.FileName = "cmd.exe";
                proc.StartInfo.UseShellExecute = false;
                proc.StartInfo.RedirectStandardInput = true;
                proc.StartInfo.RedirectStandardOutput = true;
                proc.StartInfo.RedirectStandardError = true;
                proc.StartInfo.CreateNoWindow = true;
                proc.Start();
                string dosLine = @"net use \\" + path + " /User:" + userName + " " + passWord + " /PERSISTENT:YES";
                proc.StandardInput.WriteLine(dosLine);
                proc.StandardInput.WriteLine("exit");
                while (!proc.HasExited)
                {
                    proc.WaitForExit(1000);
                }
                string errormsg = proc.StandardError.ReadToEnd();
                proc.StandardError.Close();
                if (string.IsNullOrEmpty(errormsg))
                {
                    Flag = true;
                }
                else
                {
                    throw new Exception(errormsg);
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            finally
            {
                proc.Close();
                proc.Dispose();
            }
            return Flag;
        }



        public static void CopyDir(string srcPath, string aimPath, Action<int, string> ShowMessage)
        {
            try
            {


                // 检查目标目录是否以目录分割字符结束如果不是则添加之
                if (aimPath[aimPath.Length - 1] != Path.DirectorySeparatorChar)
                    aimPath += Path.DirectorySeparatorChar;
                // 判断目标目录是否存在如果不存在则新建之
                if (!Directory.Exists(aimPath))
                    Directory.CreateDirectory(aimPath);
                DirectoryInfo dir = new DirectoryInfo(srcPath);
                //是文件   
                if ((dir.Attributes & FileAttributes.Directory) == 0)
                {
                    File.Copy(srcPath, aimPath + Path.GetFileName(srcPath), true);
                }
                else
                {
                    // 得到源目录的文件列表，该里面是包含文件以及目录路径的一个数组
                    //如果你指向copy目标文件下面的文件而不包含目录请使用下面的方法
                    //string[] fileList = Directory.GetFiles(srcPath);
                    string[] fileList = Directory.GetFileSystemEntries(srcPath);

                    //遍历所有的文件和目录
                    foreach (string file in fileList)
                    {
                        //先当作目录处理如果存在这个目录就递归Copy该目录下面的文件
                        if (Directory.Exists(file))
                            CopyDir(file, aimPath + Path.GetFileName(file), ShowMessage);
                        //否则直接Copy文件
                        else
                        {
                            File.Copy(file, aimPath + Path.GetFileName(file), true);
                            ShowMessage(0, file + "-->" + aimPath + Path.GetFileName(file));
                        }
                    }
                }

            }
            catch (Exception ee)
            {
                throw new Exception(ee.ToString());
            }
        }

        public static void UploadFile(ServerModel model, Action<int, string> ShowMessage)
        {
            try
            {

                if (connectState(model.ServerHost, model.ServerUserName, model.ServerPwd))
                {
                    CopyDir(model.SourceDirectory, model.DestDirectory, ShowMessage);

                }
            }
            catch (Exception ee)
            {
                throw new Exception(ee.ToString());
            }
        }
        public static void DownloadFile(ServerModel model, Action<int, string> ShowMessage)
        {
            try
            {

                if (connectState(model.ServerHost, model.ServerUserName, model.ServerPwd))
                {
                    CopyDir(model.DestDirectory, model.SourceDirectory, ShowMessage);

                }
            }
            catch (Exception ee)
            {
                throw new Exception(ee.ToString());
            }
        }
    }
}
